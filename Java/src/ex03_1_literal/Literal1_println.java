package ex03_1_literal;

public class Literal1_println {
	public static void main(String args[]) {
		System.out.println("호호하하");
		System.out.println("\"호호하하\"");
		System.out.println("호호하하\n");
		System.out.println("호호하하%n");
		System.out.println("호호\t하하");
		System.out.println("호호\\하하");
		
		
	}
}
